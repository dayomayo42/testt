import UIKit
import TinkoffASDKCore
import TinkoffASDKUI

class NewSubscriptionListController: UIViewController {

    @IBOutlet weak var descriptionLabel: UILabel!
    @IBOutlet weak var titleLabel: UILabel!
    @IBOutlet weak var typesStack: UIStackView!
    
    @IBOutlet weak var mySubscriptionView: UIView!
    
    @IBOutlet weak var mySubscriptioLabel: UILabel!
    
    var subObject: Subscription?
     
    var presenter: NewSubscriptionListPresenterProtocol!
    let configurator: NewSubscriptionListConfiguratorProtocol = NewSubscriptionListConfigurator()
    
    override func viewDidLoad() {
        super.viewDidLoad()
        configurator.configure(with: self)
        presenter.configureView()
    }
    
    @objc func subscribeAction(_ recognizer: SubscribeRecognizer) {
        presenter.subscribe(pos: recognizer.pos ?? 0)
    }
    
    @IBAction func contractOfferAction(_ sender: Any) {
        presenter.contractOfferAction()
    }
        
    @IBAction func privacyPolicyAction(_ sender: Any) {
        presenter.privacyPolicyAction()
    }
        
    @IBAction func backAction(_ sender: Any) {
        presenter.backAction()
    }
}


class SubscribeRecognizer: UITapGestureRecognizer {
    var pos: Int?
}

